package Clase11;

public class Cliente {
    
    private int mesa;
    private int comensales;

    public Cliente(int mesa, int comensales) {
        this.mesa = mesa;
        this.comensales = comensales;
    }

    public int getMesa() {
        return mesa;
    }

    public int getComensales() {
        return comensales;
    }
    
}
